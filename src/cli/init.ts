/**
 * ikbi `init` — guided first-run setup (GAP 2).
 *
 * Detects available provider API keys, asks 2-3 questions, and generates a working
 * .ikbi/ config. Designed for the operator who just installed ikbi — they type
 * `ikbi init`, answer a few prompts, and `ikbi` works.
 *
 * SECURITY: never prints secret values. Shows provider/key status as ✓/✗ only.
 */

import { createInterface } from "node:readline";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";


// ── Provider key detection ──────────────────────────────────────────────

interface ProviderStatus {
  name: string;
  envVar: string;
  set: boolean;
}

const KNOWN_PROVIDERS: Omit<ProviderStatus, "set">[] = [
  { name: "anthropic", envVar: "IKBI_ANTHROPIC_API_KEY" },
  { name: "openai", envVar: "IKBI_OPENAI_API_KEY" },
  { name: "deepseek", envVar: "IKBI_DEEPSEEK_API_KEY" },
  { name: "openrouter", envVar: "IKBI_OPENROUTER_API_KEY" },
  { name: "google", envVar: "IKBI_GOOGLE_API_KEY" },
  { name: "groq", envVar: "IKBI_GROQ_API_KEY" },
  { name: "xai", envVar: "IKBI_XAI_API_KEY" },
  { name: "ollama", envVar: "OLLAMA_HOST" },
];

function detectProviders(): ProviderStatus[] {
  return KNOWN_PROVIDERS.map((p) => ({
    ...p,
    set: process.env[p.envVar] !== undefined && process.env[p.envVar] !== "",
  }));
}

// ── Blessed recommendations ─────────────────────────────────────────────

